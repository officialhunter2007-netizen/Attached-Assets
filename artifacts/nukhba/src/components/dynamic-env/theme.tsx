export function themeForKind(kind?: string): SubjectTheme {
  return KIND_THEMES[String(kind || "generic")] || KIND_THEMES.generic;
}

export function themeCssVars(t: SubjectTheme): CSSProperties {
  return {
    ["--env-accent" as any]: t.accent,
    ["--env-accent-soft" as any]: t.accentSoft,
    ["--env-accent-text" as any]: t.accentText,
    ["--env-accent-border" as any]: t.accentBorder,
    ["--env-accent-bg" as any]: t.accentBg,
    ["--env-grad-from" as any]: t.gradFrom,
    ["--env-grad-to" as any]: t.gradTo,
    ["--env-surface" as any]: t.surface,
    ["--env-surface-border" as any]: t.surfaceBorder,
    ["--env-primary-bg" as any]: t.primaryBtnBg,
    ["--env-primary-text" as any]: t.primaryBtnText,
  };
}

const ThemeCtx = createContext<SubjectTheme>(KIND_THEMES.generic);

export function useEnvTheme(): SubjectTheme {
  return useContext(ThemeCtx);
}

export function EnvThemeProvider({ theme, children }: { theme: SubjectTheme; children: ReactNode }) {
  const value = useMemo(() => theme, [theme]);
  return <ThemeCtx.Provider value={value}>{children}</ThemeCtx.Provider>;
}
